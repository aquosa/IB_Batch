/////////////////////////////////////////////////////////////////////////////////////////////////////////
// Buscar un Codigo de Operacion adecuado , en funcion de la cantidad de palabras que tenga
// la descripcion de INPUT, tal que se buscara la mas aproximada en la descripcion detallada que
// luego se copiara como OUTPUT. Debe coincidir que sea DEBITO "D" o CREDITO "C", y para mas
// inteligencia, puede discriminar palabras en singular de otras en plural.
// Cada palabra o token encontrado, suma 1 nivel. Cuanto mas niveles, mas probabilidad de que sea la
// descripcion mas adecuada. 
// Se utiliza un algoritmico no deterministico. Sino, si se quiere 100% exactitud, habra que hacer
// una tabla de mapeos 1-a-1.
/////////////////////////////////////////////////////////////////////////////////////////////////////////
PCSTR TrxResBATCH::SearchInternalCodOpe( PCSTR szExtCodOpe, /* tipo de operacion INPUT */
                                         PCSTR szExtDescrip, /* descripcion INPUT */
                                         PCSTR szExtCRDB,    /* "D" o "C" */
                                         char *szCODOPE,  /* tipo de operacion OUTPUT */
                                         char *szDESCRIP  /* descripocion larga OUTPUT */
                                         )
{
    // Local Index in Array
	register size_t  nInx  = 0 , 
                     hTknx = 0 ;
    // Pointer to TOKENS (9 levels = 9 tokens )
    char *ptrToken[9+1]    = { NULL, NULL, NULL, NULL, NULL, NULL, NULL, NULL, NULL } ,
         *ptr              = NULL ;
    // DELIMITERS
    const char cszTokens[] = " ,;./-_&#[]()\\" ;
    // Last LEVEL
    short          shLEVEL      = 0,
                   shLAST_LEVEL = 0;
    // Last INDEX
    size_t         nLastInx     = 0;

    // Precondition 
    if( !szExtCodOpe || !szExtDescrip || !szExtCRDB )
        return NULL;
    // Precondition 
    if( !szCODOPE    || !szDESCRIP )
        return NULL;


    // Search Tokens in INPUT Description        
    ptr = strtok( 
                /*** UPPERCASE ALWAYS ***/
                strupr((LPSTR)szExtDescrip) , 
                /* List of Tokens */
                cszTokens 
                );
	for( nInx = 0; nInx < (sizeof(ptrToken)/sizeof(ptrToken[0])) ; nInx++ )
    {
        // Valid Token?
        if( NULL == ptr )
            ptr = " ";
        ptrToken[nInx] = ptr;
        // Next Token
        ptr = strtok( NULL, cszTokens);
    };

    // Check for Plural Tokens --> Convert to Singular    
	for( nInx = 0; nInx < (sizeof(ptrToken)/sizeof(ptrToken[0])) ; nInx++ )
    {        
        if( ptrToken[nInx] ) 
        {
            hTknx = strlen( ptrToken[nInx] ) ;
            if( hTknx > 1 )
            {
                if( ptrToken[nInx][ hTknx - 1 ] == 'S' 
                    ||
                    ptrToken[nInx][ hTknx - 1 ] == 's') 
                    // 's'/'S' = null
                    ptrToken[nInx][ hTknx - 1 ] = 0x00;
            }
        };
    };

    // Search Loop in Array - NON DETERMINISTIC !!!!
	for( nInx = 0; nInx < sizeof(aszCODOPE)/sizeof(aszCODOPE[0]); nInx++ )
    {
        // Sum of Levels of Match for this Comparison : EACH LEVEL, ONE TOKEN FOUND.
        for ( shLEVEL = 0, hTknx = 0; hTknx < sizeof(ptrToken)/sizeof(ptrToken[0]); hTknx ++ )
        {
	        if (strstr( aszCODOPE[nInx].DESCRIPCION, ptrToken[hTknx]) 
                /************** fix q no sea 1 blanco ***************/
                && 
                strcmp(ptrToken[hTknx], " ") 
                /************** fix q no sea 1 blanco ***************/
                )
                shLEVEL++; // ONE MORE TOKEN FOUND!
        };
        // Check Previous LEVEL and CR-DB
        if ( aszCODOPE[nInx].DEBITO != NULL 
             && 
             aszCODOPE[nInx].CREDITO != NULL 
             &&
             ( strcmp(szExtCRDB, aszCODOPE[nInx].DEBITO) || 
               strcmp(szExtCRDB, aszCODOPE[nInx].CREDITO) 
              ) 
              && 
              shLAST_LEVEL < shLEVEL)
        {
            shLAST_LEVEL = shLEVEL; /* Backup */
            nLastInx     = nInx;    /* Backup */
            COPYSTRING( szCODOPE , aszCODOPE[nInx].NRO );
            COPYSTRING( szDESCRIP, aszCODOPE[nInx].DESCRIPCION );
            // LEVEL CONDITION : IS THE MAXIMUM LEVEL?
            if( sizeof(ptrToken)/sizeof(ptrToken[0]) == shLEVEL )
                // RETURN , MAX LEVEL !
                return aszCODOPE[nInx].NRO ;
            else
                // CONTINUE TO NEXT LEVEL, IF POSSIBLE
                continue;
        };//LEVEL-PREVIOUS-LEVEL
    };
    // Check for previous "valid" values... or use the DEFAULT :
    if( 0 == shLAST_LEVEL && 0 == nLastInx )
    {
		/* FIX DEFAULT DB-CR */
		if ( 'C' == szExtCRDB[0] )
		{
			COPYSTRING( szCODOPE , aszCODOPE_DEFAULT_CR.NRO );
			COPYSTRING( szDESCRIP, aszCODOPE_DEFAULT_CR.DESCRIPCION );    
			return aszCODOPE_DEFAULT_CR.NRO;
		}
		else if ( 'D' == szExtCRDB[0] )
		{
			COPYSTRING( szCODOPE , aszCODOPE_DEFAULT_DB.NRO );
			COPYSTRING( szDESCRIP, aszCODOPE_DEFAULT_DB.DESCRIPCION );    
			return aszCODOPE_DEFAULT_DB.NRO;
		}
		else
		{
			// DEFAULT : "N47","AJUSTES","AJUSTES","D","C",
			COPYSTRING( szCODOPE , aszCODOPE_DEFAULT.NRO );
			COPYSTRING( szDESCRIP, aszCODOPE_DEFAULT.DESCRIPCION );    
		}
		/* FIX DEFAULT DB-CR */
        return aszCODOPE_DEFAULT.NRO;
    }
    // Check for a MINIMUM of concidencies : 25% effectivity at least
    else if( shLAST_LEVEL <  (sizeof(ptrToken)/sizeof(ptrToken[0])) % 4)
    {
		/* FIX DEFAULT DB-CR */
		if ( 'C' == szExtCRDB[0] )
		{
			COPYSTRING( szCODOPE , aszCODOPE_DEFAULT_CR.NRO );
			COPYSTRING( szDESCRIP, aszCODOPE_DEFAULT_CR.DESCRIPCION );    
			return aszCODOPE_DEFAULT_CR.NRO;
		}
		else if ( 'D' == szExtCRDB[0] )
		{
			COPYSTRING( szCODOPE , aszCODOPE_DEFAULT_DB.NRO );
			COPYSTRING( szDESCRIP, aszCODOPE_DEFAULT_DB.DESCRIPCION );    
			return aszCODOPE_DEFAULT_DB.NRO;
		}
		else
		{
			// DEFAULT : "N47","AJUSTES","AJUSTES","D","C",
			COPYSTRING( szCODOPE , aszCODOPE_DEFAULT.NRO );
			COPYSTRING( szDESCRIP, aszCODOPE_DEFAULT.DESCRIPCION );    
		}
		/* FIX DEFAULT DB-CR */
        return aszCODOPE_DEFAULT.NRO;
    }
    else
    {
        return aszCODOPE[nLastInx].NRO;
    };
};

